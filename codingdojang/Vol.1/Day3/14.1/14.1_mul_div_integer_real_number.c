#include <stdio.h>
int main()
{
	printf("multiple & division in integer\n");
	printf("2 * 3 = %d\n",2 * 3);
	printf("7 / 2 = %d\n",7 / 2);
	//printf("1 / 0 = %d\n", 1 / 0);
	printf("multiple & division in real number\n");
	printf("2.73f * 3.81f = %f\n",2.73f * 3.81f);
	printf("7.0f / 2.0f = %f\n",7.0f / 2.0f);
	//printf("1.0f / 0.0f = %f\n",1.0f / 0.0f);
	return 0;
}